#include <cmath>

#include <cstdio>

#include <vector>

#include <iostream>

#include <algorithm>

using namespace std;







int main() {

    long long int t,n;

    cin>>t;

    while(t-- )

    {

        cin>>n;

        long long int k=0,c=0,d=1,e=0;

        while(1)

        {

           k=c+d;

           c=d;

           d=k; 

           if(k<=n)

           {

             if(k%2==0)

             {

                e=e+k;

             }

           }

           else {

           break;

           }  

        }

        cout<<e<<"\n";
    }    

    return 0;

}
